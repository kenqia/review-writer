def build_checkpoint_log() -> list[dict[str, Any]]:
    rows = []
    for index, (name, note) in enumerate(CHECKPOINTS, start=1):
        rows.append(
            {
                "index": index,
                "checkpoint": name,
                "input_files": checkpoint_inputs(name),
                "output_files": checkpoint_outputs(name),
                "states": ["blocked", "ready_for_human_review", "approved_mock"],
                "status": "approved_mock",
                "human_review_required": True,
                "note": note,
            }
        )
    return rows


