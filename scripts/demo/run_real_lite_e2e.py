#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
import shutil
import subprocess
import sys
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parents[2]
MARKER = ".real_lite_e2e_generated"
PROJECT_ID = "real_lite_allene_review"
EXCERPT_CHARS = 650

CHECKPOINTS = [
    ("Library", "Selected real-lite package is ready for review."),
    ("Discovery", "Discovery candidates are ready for review."),
    ("Matrix", "Literature matrix and outline are ready for review."),
    ("Blueprint", "Section plan is ready for review."),
    ("Sections", "Section draft is ready for review."),
    ("Figures", "Figure pointer manifest is ready for review."),
    ("Draft", "First draft is ready for review."),
    ("Final", "Final audit and offline quality gate are ready for review."),
    ("Export", "Markdown export is ready for review."),
]


class DemoError(Exception):
    pass


def main() -> int:
    args = parse_args()
    try:
        run_demo(args.demo_root.resolve(), args.output_root.resolve(), strict=args.strict)
    except DemoError as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 2
    print(f"real-lite-e2e: wrote offline run to {args.output_root.resolve()}")
    return 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run real-lite offline end-to-end review-writer demo.")
    parser.add_argument("--demo-root", type=Path, default=Path("demo_projects/real_lite_allene_review"))
    parser.add_argument("--output-root", type=Path, default=Path("/tmp/review_writer_real_lite_e2e"))
    parser.add_argument("--strict", action="store_true")
    return parser.parse_args()


def run_demo(demo_root: Path, output_root: Path, *, strict: bool) -> None:
    if not demo_root.exists():
        raise DemoError(f"demo root not found: {demo_root}")
    inputs = demo_root / "inputs"
    selected = load_selected(inputs / "selected_papers.json")
    if len(selected) < 3:
        raise DemoError(f"real-lite run expects at least 3 selected papers, got {len(selected)}")

    prepare_output_root(output_root)
    papers = [load_paper_bundle(inputs, row) for row in selected]
    checkpoint_log = build_checkpoint_log()

    write_json(
        output_root / "project_status_before.json",
        {
            "project_id": PROJECT_ID,
            "exists": False,
            "mode": "real_lite_offline_e2e",
            "selected_count": len(papers),
            "network": "not_used",
            "pdf_read": "not_used",
        },
    )
    write_json(output_root / "checkpoint_log.json", {"project_id": PROJECT_ID, "checkpoints": checkpoint_log})

    write_discovery(output_root, papers)
    write_matrix_outline(output_root, papers)
    write_blueprint(output_root, papers)
    write_section_draft(output_root, papers)
    write_figures(output_root, papers)
    draft_path = write_first_draft(output_root, papers)
    write_final_audit(output_root, draft_path, strict=strict)
    write_export(output_root)
    write_run_summary(output_root, demo_root, papers)
    (output_root / MARKER).write_text("generated by scripts/demo/run_real_lite_e2e.py\n", encoding="utf-8")


def prepare_output_root(output_root: Path) -> None:
    if output_root.exists():
        if (output_root / MARKER).exists():
            shutil.rmtree(output_root)
        elif any(output_root.iterdir()):
            raise DemoError(f"refusing to overwrite non-demo output root: {output_root}")
    output_root.mkdir(parents=True, exist_ok=True)


def load_selected(path: Path) -> list[dict[str, Any]]:
    payload = load_json(path, "selected_papers")
    selected = payload.get("selected_papers", payload if isinstance(payload, list) else [])
    if not isinstance(selected, list):
        raise DemoError("selected_papers must be a list")
    return [row for row in selected if isinstance(row, dict)]


def load_paper_bundle(inputs: Path, selected_row: dict[str, Any]) -> dict[str, Any]:
    paper_id = str(selected_row.get("paper_id") or "").strip()
    if not paper_id:
        raise DemoError("selected paper missing paper_id")
    metadata = load_json(inputs / "paper_metadata" / f"{paper_id}.metadata.json", f"{paper_id} metadata")
    content_pointer = load_json(inputs / "content_list" / f"{paper_id}.content_list.pointer.json", f"{paper_id} content pointer")
    figure_pointer = load_json(inputs / "figures" / f"{paper_id}.figures.pointer.json", f"{paper_id} figure pointer")
    excerpt_path = inputs / "mineru_markdown" / f"{paper_id}.excerpt.md"
    if not excerpt_path.exists():
        raise DemoError(f"excerpt not found: {excerpt_path}")
    excerpt = excerpt_path.read_text(encoding="utf-8", errors="ignore")[:EXCERPT_CHARS]
    return {
        "paper_id": paper_id,
        "selected": selected_row,
        "metadata": metadata,
        "excerpt_path": str(excerpt_path),
        "excerpt_preview": normalize_excerpt(excerpt),
        "content_pointer": content_pointer,
        "figure_pointer": figure_pointer,
        "title": clean_value(metadata.get("title")) or selected_row.get("title") or paper_id,
        "year": clean_value(metadata.get("year")) or selected_row.get("year") or "",
        "authors": clean_authors(clean_value(metadata.get("authors"))),
        "journal": clean_value(metadata.get("journal")) or "",
        "doi": clean_value(metadata.get("doi")) or "",
        "tags": clean_value(metadata.get("structured_tags")) or {},
    }


def load_json(path: Path, label: str) -> Any:
    if not path.exists():
        raise DemoError(f"{label} not found: {path}")
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise DemoError(f"{label} is not valid JSON: {path} ({exc})") from exc


def clean_value(value: Any) -> Any:
    if isinstance(value, dict) and "value" in value:
        return clean_value(value["value"])
    if isinstance(value, list):
        return [clean_value(item) for item in value]
    return value


def clean_authors(value: Any) -> list[str]:
    if isinstance(value, list):
        return [str(item).strip() for item in value if str(item).strip()][:6]
    if isinstance(value, str) and value.strip():
        return [value.strip()]
    return ["Unknown authors"]


def normalize_excerpt(text: str) -> str:
    lines = []
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("<!--"):
            continue
        lines.append(stripped)
    preview = " ".join(lines)
    return " ".join(preview.split())[:EXCERPT_CHARS]


def write_discovery(output_root: Path, papers: list[dict[str, Any]]) -> None:
    candidates = []
    for index, paper in enumerate(papers):
        candidates.append(
            {
                "paper_id": paper["paper_id"],
                "title": paper["title"],
                "year": paper["year"],
                "score": round(0.94 - index * 0.03, 2),
                "has_metadata": True,
                "has_excerpt": True,
                "has_content_pointer": bool(paper["content_pointer"].get("source_path")),
                "has_figure_pointer": bool(paper["figure_pointer"].get("source_path")),
                "checkpoint_status": "approved_mock",
            }
        )
    write_json(output_root / "00_discovery/discovery_candidates.json", {"topic": topic_text(), "candidates": candidates})


def write_matrix_outline(output_root: Path, papers: list[dict[str, Any]]) -> None:
    rows = []
    for ref_id, paper in enumerate(papers, start=1):
        tags = paper.get("tags") if isinstance(paper.get("tags"), dict) else {}
        rows.append(
            {
                "ref_id": ref_id,
                "paper_id": paper["paper_id"],
                "title": paper["title"],
                "year": paper["year"],
                "journal": paper["journal"],
                "catalyst_or_method": tags.get("catalyst_or_method") or "",
                "reaction_type": tags.get("reaction_type") or "",
                "excerpt_preview": paper["excerpt_preview"],
            }
        )
    write_json(output_root / "01_matrix_outline/literature_matrix.json", {"rows": rows, "checkpoint_status": "approved_mock"})
    outline = "\n".join(
        [
            "# Real-Lite Allene Review Outline",
            "",
            "## Catalytic Entry Points For Allenyl Products",
            "Compare the selected palladium, copper, nickel, and gold examples.",
            "",
            "## Selectivity And Scope Signals",
            "Summarize scope and selectivity signals that can be audited by a human reviewer.",
            "",
        ]
    )
    (output_root / "01_matrix_outline").mkdir(parents=True, exist_ok=True)
    (output_root / "01_matrix_outline/outline.md").write_text(outline, encoding="utf-8")


