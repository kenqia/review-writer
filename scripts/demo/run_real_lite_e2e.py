            "",
            "This real-lite draft summarizes five selected records on allene-centered synthesis and catalysis [1-5]. "
            "The set includes palladium carbonylation, photoredox copper phosphorothiolation, a total synthesis case, asymmetric palladium allenylation, and nickel reductive coupling.",
            "",
