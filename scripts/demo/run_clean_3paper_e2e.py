def references(papers: list[dict[str, Any]]) -> list[str]:
    rows = []
    for ref_id, paper in enumerate(papers, start=1):
        authors = authors_text(paper["authors"])
        doi = paper["doi"] if paper["doi"] and paper["doi"] != "unknown" else "needs human review"
        rows.append(f"[{ref_id}] {authors}. {paper['title']}. {paper['journal']} {paper['year']}. DOI: {doi}.")
    return rows


def write_final_audit(output_root: Path, draft_path: Path, papers: list[dict[str, Any]], *, strict: bool) -> None:
    out = output_root / "05_final_audit"
    out.mkdir(parents=True, exist_ok=True)
    final_draft = out / "final_draft.md"
    shutil.copyfile(draft_path, final_draft)
    quality_json = out / "quality_report.json"
    quality_md = out / "quality_report.md"
    cmd = [
        sys.executable,
        str(REPO_ROOT / "scripts/validators/validate_review_quality.py"),
        "--draft",
        str(final_draft),
        "--figure-manifest",
        str(output_root / "03_figure_redraw/figure_manifest.json"),
        "--judge-mode",
        "offline",
        "--output-json",
        str(quality_json),
        "--output-md",
        str(quality_md),
    ]
    result = subprocess.run(cmd, cwd=REPO_ROOT, text=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE, check=False)
    if result.returncode not in {0, 1}:
        raise CleanE2EError(f"quality validator failed: {result.stderr.strip() or result.stdout.strip()}")
    quality = load_json(quality_json)
    warnings = list(quality.get("warnings") or [])
    for paper in papers:
        if paper["missing_fields"]:
            warnings.append({"rule_id": "CLEAN3_METADATA_WARNING", "paper_id": paper["paper_id"], "message": f"missing fields: {', '.join(paper['missing_fields'])}"})
        if paper["source_conflicts"]:
            warnings.append({"rule_id": "CLEAN3_SOURCE_CONFLICT", "paper_id": paper["paper_id"], "message": "source conflicts require human review"})
    quality["warnings"] = warnings
    quality["status"] = "warn" if warnings and quality.get("status") == "pass" else quality.get("status", "warn")
    quality["trusted_for_scientific_quality"] = False
    write_json(quality_json, quality)
    write_quality_markdown(quality_md, quality)
    errors = quality.get("errors") or []
    if strict and errors:
        raise CleanE2EError(f"quality gate has critical errors: {len(errors)}")
    write_json(
        out / "final_audit_report.json",
        {
            "project_id": PROJECT_ID,
            "quality_status": quality.get("status"),
            "quality_errors": len(errors),
            "quality_warnings": len(warnings),
            "final_quality_gate_enforced": True,
            "clean_draft": True,
            "needs_human_review": True,
            "not_final_scientific_review": True,
            "trusted_for_scientific_quality": False,
            **safety_markers(),
        },
    )
    write_review_pack(out / "clean_3paper_review_pack.md", papers, quality)


def write_quality_markdown(path: Path, quality: dict[str, Any]) -> None:
    lines = ["# Clean 3-Paper Quality Report", "", f"- status: {quality.get('status')}", ""]
    if quality.get("warnings"):
        lines.append("## Warnings")
        for item in quality["warnings"]:
            lines.append(f"- {item}")
    lines.append("")
    lines.append("trusted_for_scientific_quality: false")
    write_text(path, "\n".join(lines))


def write_review_pack(path: Path, papers: list[dict[str, Any]], quality: dict[str, Any]) -> None:
    lines = [
        "# Clean 3-Paper Review Pack",
        "",
        "Status: clean_draft; needs_human_review; not_final_scientific_review.",
        "",
        "## Papers",
    ]
    for paper in papers:
        lines.extend(
            [
                f"### {paper['paper_id']}",
                f"- title: {paper['title']}",
                f"- authors: {authors_text(paper['authors'])}",
                f"- year/journal/doi: {paper['year']} / {paper['journal']} / {paper['doi']}",
                f"- missing_fields: {', '.join(paper['missing_fields']) or 'none'}",
                f"- source_conflicts: {len(paper['source_conflicts'])}",
                "- human_verified: false",
                "- needs_human_review: true",
                "",
                "Claim drafts:",
            ]
        )
        for claim in paper["claims"]:
            lines.append(f"- {claim['claim_id']}: {claim['claim_text_draft']} ({claim['confidence']})")
        lines.append("")
    lines.extend(["## Quality Gate", "", f"- status: {quality.get('status')}", f"- warnings: {len(quality.get('warnings') or [])}", ""])
    write_text(path, "\n".join(lines))


