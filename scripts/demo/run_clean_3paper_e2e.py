REPO_ROOT = Path(__file__).resolve().parents[2]
PROJECT_ID = "clean_3paper_allene_review"
MARKER = ".clean_3paper_e2e_generated"
TOP3 = ["F3I", "F47A", "P403"]

CHECKPOINTS = [
    ("Library", "Clean draft input package is ready for human review."),
    ("Discovery", "Approved Top 3 discovery candidates are ready for human review."),
    ("Matrix", "Clean draft literature matrix and outline are ready for human review."),
    ("Blueprint", "Section plan is ready for human review."),
    ("Sections", "Section draft is ready for human review."),
    ("Figures", "Figure note manifest is ready for human review."),
    ("Draft", "Clean draft manuscript is ready for human review."),
    ("Final", "Final audit and quality gate are ready for human review."),
    ("Export", "Markdown export is ready for human review."),
]


class CleanE2EError(Exception):
    pass


def main() -> int:
    args = parse_args()
    try:
        run_clean_e2e(args.demo_root.resolve(), args.output_root.resolve(), strict=args.strict)
    except CleanE2EError as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 2
    print(f"clean-3paper-e2e: wrote vertical slice to {args.output_root.resolve()}")
    return 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run clean 3-paper offline vertical-slice E2E.")
    parser.add_argument("--demo-root", type=Path, default=Path("demo_projects/clean_3paper_allene_review"))
    parser.add_argument("--output-root", type=Path, default=Path("/tmp/review_writer_clean_3paper_e2e"))
    parser.add_argument("--strict", action="store_true")
    return parser.parse_args()


def run_clean_e2e(demo_root: Path, output_root: Path, *, strict: bool) -> None:
    if not demo_root.exists():
        raise CleanE2EError(f"demo root not found: {demo_root}")
    inputs = demo_root / "inputs"
    expected = demo_root / "expected"
    papers = load_clean_papers(inputs, expected)
    write_clean_input_package(demo_root, papers)
    prepare_output_root(output_root)
    write_project_status(output_root, demo_root, papers)
    write_json(output_root / "checkpoint_log.json", {"project_id": PROJECT_ID, "checkpoints": build_checkpoint_log()})
    write_discovery(output_root, papers)
    write_matrix_outline(output_root, papers)
    write_blueprint(output_root, papers)
    write_section(output_root, papers)
    write_figures(output_root, papers)
    draft_path = write_first_draft(output_root, papers)
    write_final_audit(output_root, draft_path, papers, strict=strict)
    write_eval_placeholder(output_root)
    write_export(output_root)
    write_run_summary(output_root, demo_root, papers)
    (output_root / MARKER).write_text("generated by scripts/demo/run_clean_3paper_e2e.py\n", encoding="utf-8")


def load_clean_papers(inputs: Path, expected: Path) -> list[dict[str, Any]]:
    selected = load_json(inputs / "selected_papers.verified_draft.json")
    bibliography = load_json(inputs / "bibliography_verification_summary.json")
    claims_payload = load_json(expected / "expected_claims.draft.json")
    figures_payload = load_json(expected / "expected_figures.draft.json")
    selected_rows = selected.get("papers") or []
    bibliography_rows = {row["candidate_id"]: row for row in bibliography.get("papers") or []}
    claims_by_id = group_by(claims_payload.get("claims") or [], "paper_id")
    figures_by_id = group_by(figures_payload.get("figure_notes") or [], "paper_id")
    if [row.get("candidate_id") for row in selected_rows] != TOP3:
        raise CleanE2EError("selected clean draft does not contain expected Top 3")
    papers: list[dict[str, Any]] = []
    for row in selected_rows:
        paper_id = row["candidate_id"]
        biblio = bibliography_rows.get(paper_id, {})
        claims = claims_by_id.get(paper_id, [])
        figures = figures_by_id.get(paper_id, [])
        if row.get("human_verified") is not False or row.get("needs_human_review") is not True:
            raise CleanE2EError(f"{paper_id}: clean draft must remain human_verified=false and needs_human_review=true")
        papers.append(
            {
                "paper_id": paper_id,
                "filename": row.get("filename") or biblio.get("filename"),
                "title": biblio.get("verified_title_draft") or row.get("title_draft") or paper_id,
                "authors": biblio.get("authors_draft") or row.get("authors_draft") or [],
                "year": biblio.get("year_draft") or row.get("year_draft") or "unknown",
                "journal": biblio.get("journal_draft") or row.get("journal_draft") or "unknown",
                "doi": biblio.get("doi_draft") or row.get("doi_draft") or "unknown",
                "role": row.get("role") or "unknown",
                "metadata_confidence": biblio.get("metadata_confidence") or row.get("metadata_confidence") or "low",
                "source_conflicts": biblio.get("conflicts") or row.get("source_conflicts") or [],
                "missing_fields": biblio.get("missing_fields") or row.get("missing_fields") or [],
                "claims": claims,
                "figure_notes": figures,
                "verification_status": "clean_draft",
                "human_verified": False,
                "needs_human_review": True,
            }
        )
    return papers


def write_clean_input_package(demo_root: Path, papers: list[dict[str, Any]]) -> None:
    inputs = demo_root / "inputs"
    claims_dir = inputs / "claims"
    claims_dir.mkdir(parents=True, exist_ok=True)
    clean_rows = []
    for paper in papers:
        clean_row = {
            "paper_id": paper["paper_id"],
            "candidate_id": paper["paper_id"],
            "title": paper["title"],
            "authors": paper["authors"] or "unknown",
            "year": paper["year"],
            "journal": paper["journal"],
            "doi": paper["doi"],
            "role": paper["role"],
            "verification_status": "clean_draft",
            "human_verified": False,
            "needs_human_review": True,
            "metadata_confidence": paper["metadata_confidence"],
            "source_conflicts": paper["source_conflicts"],
            "missing_fields": paper["missing_fields"],
            "claims_path": f"inputs/claims/{paper['paper_id']}.claims.draft.json",
            "figure_notes_path": f"inputs/figure_notes/{paper['paper_id']}.figure_notes.md",
        }
        clean_rows.append(clean_row)
        write_json(
            claims_dir / f"{paper['paper_id']}.claims.draft.json",
            {
                "paper_id": paper["paper_id"],
                "claims": paper["claims"],
                "verification_status": "clean_draft",
                "human_verified": False,
                "needs_human_review": True,
                "trusted_for_scientific_quality": False,
            },
        )
    write_json(
        inputs / "selected_papers.clean_draft.json",
        {
            "dataset_id": "clean_3paper_allene_v1",
            "status": "clean_draft",
            "papers": clean_rows,
            "trusted_for_scientific_quality": False,
            "human_verified": False,
            "needs_human_review": True,
            "warnings": clean_warnings(papers),
            "safety": safety_markers(),
        },
    )
    lines = []
    for row in clean_rows:
        lines.append(json.dumps(row, ensure_ascii=False))
    (inputs / "clean_registry.jsonl").write_text("\n".join(lines) + "\n", encoding="utf-8")


def clean_warnings(papers: list[dict[str, Any]]) -> list[str]:
    warnings: list[str] = []
    for paper in papers:
        if paper["missing_fields"]:
            warnings.append(f"{paper['paper_id']}: missing fields: {', '.join(paper['missing_fields'])}")
        if paper["source_conflicts"]:
            warnings.append(f"{paper['paper_id']}: source conflicts require human review")
    return warnings


def prepare_output_root(output_root: Path) -> None:
    if output_root.exists():
        if (output_root / MARKER).exists():
            shutil.rmtree(output_root)
        elif any(output_root.iterdir()):
            raise CleanE2EError(f"refusing to overwrite non-clean-demo output root: {output_root}")
    output_root.mkdir(parents=True, exist_ok=True)


